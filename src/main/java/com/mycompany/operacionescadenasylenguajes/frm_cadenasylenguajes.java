        String[] l2Array = l2Texto.split(",");

        Set<String> conjuntoL1 = new LinkedHashSet<>();
        Set<String> conjuntoL2 = new LinkedHashSet<>();
        Set<String> diferencia = new LinkedHashSet<>();

